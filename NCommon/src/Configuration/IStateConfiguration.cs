using NCommon.DependencyInjection;

namespace NCommon.Configuration
{
    /// <summary>
    /// Interface that can be implemented by classes that provide state configuration for NCommon.
    /// </summary>
    public interface IStateConfiguration
    {
        /// <summary>
        /// Called by NCommon <see cref="Configure"/> to configure state storage.
        /// </summary>
        /// <param name="containerAdapter">The <see cref="IContainerAdapter"/> instance that can be
        /// used to register state storage components.</param>
        void Configure(IContainerAdapter containerAdapter);
    }
}